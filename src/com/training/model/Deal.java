package com.training.model;

public class Deal {

	private Integer id;
	private String dealId;
	private String dealStepId;
	public String getDealId() {
		return dealId;
	}
	public void setDealId(String dealId) {
		this.dealId = dealId;
	}
	public String getDealStepId() {
		return dealStepId;
	}
	public void setDealStepId(String dealStepId) {
		this.dealStepId = dealStepId;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
}
